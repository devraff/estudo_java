package Main;

public class estrutura_condicional {

	public static void main(String[] args) {
		int x = 5;
		int y = 0;
		int z = 10;
		
		if (x > 0) {
			System.out.println("Bom dia");
			
		}
		
		if (y > 0) {
			System.out.println("Boa tarde");
			
			
		}
		
		if (z > 0) {
			System.out.println("Boa noite");
		}
	}

}




